print(type(15)) # class '<int>' -> int -> integer -> Número entero
print(type(15.8)) # float -> punto flotante -> número real
print(type(15j)) # complex -> número complejo
print(type("15")) # str -> string -> texto
print(type([16,"hola",3])) # list -> lista -> Conjunto ordenado y mutable de datos
print(type((16,"hola",3))) # tuple -> tupla -> Conjunto ordenado e inmutable de datos
print(type({"hola": 15})) # dict -> diccionario -> Conjunto nombrado de cosas
print(type(True)) # bool -> booleanos -> verdadero y falso